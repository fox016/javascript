/*
 * Play a song one note at a time
 * @param listOfNotes - list of simple note objects (properties: note, octave, and beats)
 * @param msPerWhole - optional, milliseconds per whole note, default 1500
 * @param msBetweenNotes - optional, milliseconds between notes played, default 10
 */
function playNotesInOrder(listOfNotes, msPerWhole, msBetweenNotes)
{
  if(listOfNotes.length == 0)
    return;
  if(typeof msPerWhole !== "number")
    msPerWhole = 1500;
  if(typeof msBetweenNotes !== "number")
    msBetweenNotes = 10;

  let n = listOfNotes.shift();

  if(n.note == "br")
  {
    setTimeout(function() { playNotesInOrder(listOfNotes, msPerWhole, msBetweenNotes); }, n.beats * msPerWhole);
    return;
  }

  let noteObj = $("#piano .note[data-name='"+n.note+"']")[0];
  $("#octave").val(n.octave);
  clickNote(noteObj);
  setTimeout(function()
  {
    unclick();
    setTimeout(function() { playNotesInOrder(listOfNotes, msPerWhole, msBetweenNotes); }, msBetweenNotes);
  }, n.beats * msPerWhole);
}

/*
 * Play a complex song that allows multiple notes at a time
 * @param listOfNotes - list of complex note objects (properties: measure, start, note, octave, and beats)
 *  Must pass in sorted by measure, start properties (ascending)
 * @param beatsPerMeasure - optional, percentage of a whole note per measure (e.g. 0.5 for 2/4 time signature), default 1
 * @param msPerWhole - optional, milliseconds per whole note, default 1500
 * @param msBetweenNotes - optional, milliseconds between notes played, default 10
 */
function playComplexSong(listOfNotes, beatsPerMeasure, msPerWhole, msBetweenNotes)
{
  if(typeof beatsPerMeasure !== "number")
    beatsPerMeasure = 1;
  if(typeof msPerWhole !== "number")
    msPerWhole = 1500;
  if(typeof msBetweenNotes !== "number")
    msBetweenNotes = 10;

  for(let i = 0; i < listOfNotes.length; i++)
  {
    let n = listOfNotes[i];
    let noteObj = $("#piano .note[data-name='"+n.note+"']")[0];
    let startTime = (msPerWhole * n.measure * beatsPerMeasure) + (n.start * msPerWhole);
    setTimeout(function()
    {
      $("#octave").val(n.octave);
      pressKey($(noteObj).data('keycode'));
      setTimeout(function() {
        releaseKey($(noteObj).data('keycode'));
      }, n.beats * msPerWhole - msBetweenNotes);
    }, startTime);
  }
}

